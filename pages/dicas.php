            <h4>Dicas:</h4>

            <p>A dengue é uma doença virótica transmitida pela picada do mosquito Aedes aegypti

            e pode ser mortal. O mosquito começa a se reproduzir durante os meses mais

            quentes, quando as chuvas são mais abundantes. A reprodução se dá justamente

            em poças de água limpa e parada. O mosquito põe os ovos e depois de algumas

            semanas as larvas eclodem. O verão é a época de maior incidência da doença e,

            para prevenir a transmissão, algumas medidas são necessárias. Leia algumas dicas

            para ter um verão com muita saúde e sem o perigo da dengue..</p>

            <div class="col-sm-3">
                <img src="assets/img/1.png" align="left" class="img-responsive thumbnail" alt="Imagem responsiva"><p>
            </div>

            <div class="col-sm-9">
                1. Não deixe acúmulo de água. A água da chuva pode se acumular em garrafas, pneus, ou qualquer outro reservatório. Após os períodos de chuva, verifique se não ficou água

                acumulada em algum local..</p>

                <p>2. Ponha areia nos vasos das plantas. Em vez de usar água para as plantas, use areia

                ou pó de café nos pires dos vasos e, então, coloque água. A água contida é suficiente para

                manter as plantas vivas, mas sem ser um ponto de depósito dos ovos do mosquito da

                dengue..</p>

                <p>3. Faça furos nos pneus velhos. Os furos permitem que a água acumulada escorra, não

                ficando parada e, assim, evitando que o mosquito se reproduza..</p>

                <p>4. Cuidado com a caixa d’água. A caixa d’água é um excelente reservatório para os ovos

                da dengue. Mantenha-a sempre fechada e a limpe frequentemente com produtos

                especializados para a limpeza de caixas. Isso também vale para poços, cisternas e

                caçambas que se acumulam água..</p>
            </div>
            <p>5. Remova folhas e galhos das calhas. Esses objetos, assim como outros (flores,

            pedaços de garrafa, etc) impedem que água escoe e então, se acumula. Verifique

            semanalmente o estado de calhas, canos e ralos..</p>
            

            <p>6. Evite cultivar plantas aquáticas. A água das plantas aquáticas é limpa e propícia para

            a reprodução da dengue. Durante o pico da dengue, plante outros tipos de planta..</p>

            <p>7. Mantenha latas e garrafas emborcadas para baixo. Isso evita que a água da chuva

            se acumule e fique parada por muito tempo. O ideal é jogar garrafas, latas e latões fora ou

            não deixá-los expostos..</p>

            <div class="col-sm-3">
                <img src="assets/img/5.png" align="right" class="img-responsive thumbnail" alt="Imagem responsiva"><p>
            </div>
            
            <div class="col-sm-9">
            <p>8. Use telas protetoras. A tela protetora evita que os mosquitos entrem na sua casa, mas

            não impedem que ele se reproduza. O uso de telas e tecidos nas janelas é uma medida

            complementar e deve ser associada às outras práticas para evitar a reprodução do Aedes..</p>

            <p>9. Cuide das piscinas. As piscinas são normalmente difíceis de tratar por possuem um

            volume grande de água. Se você não a está utilizando, cubra-a com uma lona. Trate a

            água da piscina com cloro e outros desinfetantes de água..</p>

            <p>10. Preste atenção ao lixo. Muitas pessoas pensam que os lixos, por acumularem água

            suja, não apresentam perigo à dengue. Mas a verdade é que se há água acumulada, há a

            possibilidade de reprodução do mosquito. Para isso, vede os sacos de lixo e não os deixe

            expostos.</p>
